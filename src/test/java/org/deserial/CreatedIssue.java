package org.deserial;

public class CreatedIssue {
 private int id;
 private String key;
 private String self;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getKey() {
	return key;
}
public void setKey(String key) {
	this.key = key;
}
public String getSelf() {
	return self;
}
public void setSelf(String self) {
	this.self = self;
}
}
